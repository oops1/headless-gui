package widget

import "image"

// Widget — базовый интерфейс для всех UI-элементов.
//
// Виджеты организуются в дерево: корневой виджет рендерится первым,
// затем рекурсивно его потомки. Каждый виджет сам отвечает за рендеринг
// дочерних элементов.
type Widget interface {
	// Draw рисует виджет и все его потомки в переданный контекст.
	Draw(ctx DrawContext)

	// Bounds возвращает ограничивающий прямоугольник виджета
	// в абсолютных координатах холста.
	Bounds() image.Rectangle

	// SetBounds задаёт положение и размер в абсолютных координатах.
	SetBounds(r image.Rectangle)

	// Children возвращает срез дочерних виджетов.
	Children() []Widget

	// AddChild добавляет дочерний виджет в конец списка.
	AddChild(w Widget)
}
