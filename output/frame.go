// Package output определяет типы данных для вывода headless-движка.
//
// Движок рендерит UI в off-screen буфер и сравнивает его с предыдущим
// кадром. Изменившиеся блоки 64×64 пикселя передаются через канал
// в виде Frame, содержащего список DirtyTile.
package output

import "time"

// TileSize — сторона квадратного тайла в пикселях.
const TileSize = 64

// DirtyTile описывает один изменившийся блок пикселей.
//
// Data содержит сырые RGBA-байты в порядке строк сверху вниз:
//
//	[R G B A  R G B A  ...]  длина = W * H * 4 байт
//
// Для тайлов у правого/нижнего края холста W и H могут быть меньше TileSize.
type DirtyTile struct {
	X, Y int    // координаты левого верхнего угла в пространстве холста
	W, H int    // фактический размер блока (≤ TileSize)
	Data []byte // сырые RGBA-байты, W*H*4 байт
}

// Frame — результат одного рендер-тика.
// Содержит только те тайлы, которые изменились с предыдущего кадра.
// Если Tiles == nil — кадр пустой (UI не изменился).
type Frame struct {
	Seq       uint64      // монотонно возрастающий номер кадра
	Timestamp time.Time   // момент завершения рендеринга
	Tiles     []DirtyTile // изменившиеся тайлы
}
